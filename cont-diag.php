                <h1>Diagnóstico Genético Preimplantacional</h1>
                 
                <p>DGP o PGD de sus siglas en inglés, es una técnica de prevención que se utiliza en reproducción asistida con la finalidad de detectar anomalías genéticas en los embriones. el primer paso es realizar un ciclo de ICSI para posteriormente realizar un estudio genético de los embriones en día 3 o dia 5 de desarrollo embrionario, determinando así la selección de aquellos que sean genéticamente normales para transferirlos al útero.</p>
                
                
                
