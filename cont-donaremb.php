                <h1>Donación de Embriones</h1>
                 
                <p>Cuando los dos miembros de la pareja están afectados por un problema de absoluta esterilidad, se puede recurrir a la Donación de Embriones.</p> 
                
<p>Generalmente, estos embriones proceden de parejas que estuvieron en tratamientos de fertilización in vitro convencional (FIV) o con FIV – ICSI y que consiguieron el embarazo, Posterior a ello, al haber conseguido el número de hijos deseado y al disponer de embriones sobrantes, autorizan su donación a otras pacientes. </p>

<p>También se dan casos de parejas, o mujeres solteras que reciben óvulos y los fecundan con semen de donante, consiguiendo embriones in vitro. Con esta técnica, actualmente, se están consiguiendo gestaciones del 90% con dos ciclos.</p>
                
