               <h5> ¿Que es Infertilidad? </h5>
               
               <img class="art" src="img/incubadora.jpg" alt="">
               
               <p>La infertilidad se define como la imposibilidad de lograr un embarazo despues de un año de estarlo buscando intensionalmente sin utilizar método alguno de planificación familiar. Lo anterior, si la mujer es menor de 35 años de edad. Si la mujer es mayor de 35 años de edad, con seis meses de búsqueda de embarazo, y no lograrlo, es suficiente para establecer el diagnóstico de infertilidad</p>
               <p></p>
               <p></p>
               <p></p>
               <p></p>
               <p></p>
               <h2>La infertilidad se clasifica en:</h2>
               
               <ul>
                   <li>Infertilidad Primaria: Cuando nunca se ha logrado un embarazo.</li>
                   <li>Infertilidad Secundaria: Cuando se ha logrado embarazos previamente y no se puede volver a lograr (abortos previos, parto prematuro, cesárea, embarazo ectópico, etc.) </li>
               </ul>
               
               <p>En México, como en el mundo se dice que alrrededor del 15% de las parejas o mujeres en edad reproductiva pueden presentar el diagnóstico de infertilidad</p>
               
               
