               <h5> ¿Que es una Pérdida gestacional recurrente? </h5>
               
                       
               <p>Llamada también aborto recurrente o abortadora habitual, se define como la pérdida de dos o más embarazos consecutivos antes de las 20 semanas.</p>
               
               <h5> ¿Que es fallo de implantación? </h5>
               
               <p>Se diagnostica cuando no se ha podido lograr el embarazo despues de al menos tres ciclos de fecundación in vitro (FIV) o de transferencia de mas de 10 embriones de buena calidad.</p>
               
               
