                <h1>Inseminación Artificial</h1>
                 
<p>                La Inseminación Artificial (IA) consiste básicamente en la colocación del semen dentro del útero, después de haber sido tratado adecuadamente en el laboratorio. Este procedimiento es de los más sencillos y generalmente se realizarán como máximo cuatro ciclos de inseminación antes de emplear otros métodos más complejos.
</p>
    
<p>La realización de la IA no supone más molestia que una simple exploración ginecológica de rutina y sus resultados varían en función del diagnóstico y las características del semen. 
</p>

<h2>La IA puede ser
</h2>

<ul><li>Inseminación artificial conyugal (IAC), si los espermatozoides proceden de su pareja</li>
<li>Inseminación artificial de donante (IAD), en casos de mujer sola o con pareja pero con problemas masculinos que así lo determinen.</li></ul>

<p>Todos los donantes de semen son mayores de edad, asumen el anonimato y están permanentemente estudiados para descartar problemas congénitos o malformaciones y enfermedades de transmisión sexual. El semen de donante se selecciona en función del grupo sanguíneo y las características físicas de la pareja que lo solicita.
</p>

<h2>Nuestra tasa de embarazo acumulada después de 4 ciclos es:</h2>

<ul><li>60% para las IAC</li>
    <li>80 % para las IAD, variando en función del diagnóstico y evolución de cada paciente.</li>
</ul>

<h2>El Procedimiento consta de tres fases:</h2>

<ul>
<li>Protocolo de estimulación para inseminación:</li>
<li>Preparacion seminal.</li>
<li>Inseminación Intrauterina.</li>
</ul>
                
