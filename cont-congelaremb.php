<p>                El Instituto Sonorense de Infertilidad, es un centro multidiciplinar, pionero en Cd.Obregón para el estudio de la Infertilidad.Nuestra motivación es ofrecer soluciones a aquellas personas con dificultades para concebir de forma natural. La infertilidad puede originarse por diferentes factores, tanto masculino, como femenino, y en ocaciones converger en un factor mixto.
</p>
<p>Nuestra dinámica de trabajo implica un estudio completo y detallado de ambas partes de la pareja por medio de diferentes pruebas diagnósticas.
</p>
<p>El equipo biomédico ha sido formado con todas las garantías en centros de prestigio internacional tanto en México, así como en Cuba y España y abarca todas las especialidades. Contamos con un equipo de especialistas en Medicina Reproductiva de amplia experiencia en este campo, formado por Ginecólogos, Embriólogos, Andrólogos, Genetistas, Anestesistas, Enfermeros, Auxiliares de clínica y Personal administrativo con una idea en común, realizar la atención personalizada a cada paciente.
</p>
<p>La Unidad de Reproducción de ciudad obregón es una de las unidades mas avanzadas de la región, abarcando todas las técnicas de reproducción incluso las más novedosas, así mismo se mantiene vinculado a la Investigación y Formación Continua de nuestro equipo humano como pilares de su desarrollo.
</p>
