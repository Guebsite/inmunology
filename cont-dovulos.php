                <h1>Donación de óvulos</h1>
                 
<p>                Este tratamiento está indicado en mujeres que, por diferentes razones no tienen ovocitos capaces de dar lugar a un embrión evolutivo y sano. Consiste en realizar la Fecundación In Vitro con los óvulos de una donante joven cuyas edades oscilan entre 18 y 25 años con el semen de la pareja de la receptora, transfiriendo después los embriones a la paciente receptora.
</p>

<h2>Esta técnica estaría indicada en:</h2>
<ul>
<li>pacientes con edad avanzada</li>
 <li>fallo ovárico oculto</li>
<li>anomalías genéticas</li>
<li>abortos repetidos</li>
<li>menopausia…</li>
</ul>
<p>Este proceso requiere una primera visita en consulta externa donde se realizan historia clínica, análisis clínico y exploración física necesarias para asegurar el proceso de transferencia. La paciente receptora recibe un tratamiento hormonal, preparando así al útero para recibir los embriones. Esto requiere una gran coordinación con la donante, consiguiendo así unos buenos resultados. No es un proceso doloroso y no hay que esperar mucho tiempo ya que nuestro programa de donación asegura una transferencia entre las 3-4 semanas desde la preparación uterina. 
</p>
<p>La donación de óvulos está regulada legalmente y es anónima, se requiere unos consentimientos informados que deben ser firmados por ambas partes. Las donantes tienen que haber superado la mayoría de edad, son mujeres jóvenes y sanas, carecen de enfermedades genéticas y desean ayudar a otras mujeres a ser madres. La selección de la donante se realiza en función del grupo sanguíneo y las características físicas de la pareja receptora que lo solicita.</p>

<p>Con esta técnica, actualmente, se están consiguiendo gestaciones del 90% en dos ciclos transfiriendo dos embriones, no llegando al 15% de embarazos gemelares. Es la técnica que mayores posibilidades tiene por ciclo ya que los embriones tienen un gran potencial de implantación debido a la edad de la donante.
</p>
<h2>Ovodonación: Seguridad y confianza</h2>

<p>Seguridad y tranquilidad: A las donantes se le realiza un examen Psicológico por nuestra especialista. Asimismo se le realiza cariotipo en sangre periférica en búsqueda de alguna anomalía cromosómica, también le realizamos serología que cosiste en análisis de sangre en búsqueda de HIV 1 y 2, Hepatitis B y C, VDRL, grupo sanguíneo y factor, consiguiendo con ello dotar de una mayor confianza y tranquilidad en todo lo relativo a este bebé y su salud
</p>

<p>Anonimato: la donación es anónima, es imposible tanto por el receptor como por la donante el ponerse en contacto entre ellos, salvaguardando con ello su privacidad
</p>

<p>Gratuidad: Este servicio se realiza sin coste alguno.</p>

