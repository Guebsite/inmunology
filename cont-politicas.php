                <h1>Políticas de Privacidad</h1>
                 
<p>                De acuerdo a lo Previsto en la “Ley Federal de Protección de Datos Personales”, declara INSTITUTO SONORENSE DE INFERTILIDAD, ser una empresa legalmente constituida de conformidad con las leyes mexicanas, con domicilio en calle Nainari, nº 1028 y como responsable del tratamiento de sus datos personales, hace de su conocimiento que la información de nuestros clientes es tratada de forma estrictamente confidencial por lo que al proporcionar sus datos personales, tales como:
</p>

<ul>
<li>1. Nombre Completo.</li>
<li>2. Dirección.</li>
<li>3. Registro Federal de Contribuyentes.</li>
<li>4. Teléfonos de Hogar, Oficina y móviles</li>
<li>5. Correo Electrónico.</li>
<li>6. Datos relativos a sexo, edad, estado de salud, información genética y médica</li>
</ul>

<h2>Estos serán utilizados única y exclusivamente para los siguientes fines:
</h2>

<ul>
<li>1.- Proveer los servicios, tratamientos y productos requeridos por los titulares.</li>
<li>2.- Dar cumplimiento a las obligaciones contraídas adquiridas con nuestros clientes.</li>
<li>3.- Campañas de Publicidad y Fidelidad.</li>
<li>4.- Actualización de la Base de Datos.</li>
<li>5.- Evaluar la calidad y servicio.</li>
<li>6,- Realizar estudios internos sobre hábitos de consumo.</li>
<li>7.- Envío de información solicitada o que pueda ser de interés para los titulares.</li>
<li>8.- Cualquier finalidad análoga o compatible con las anteriores.</li>
</ul>

<h2>En el caso de Datos sensibles, tales como:
</h2>

<ul>
<li>1.- Datos Financieros (Ingresos, Estados de Cuenta, y demás relacionados)</li>
<li>2.- Datos Patrimoniales (Bienes Materiales, Inmuebles, y demás relacionados)</li>
<li>3.- Datos Personales (Cónyuge, Estado Civil, Nacionalidad, Educación, Hijos, y demás relacionados).</li>
<li>4.- Referencias familiares y no familiares (Nombre, Dirección, Teléfono, relación, etc.).</li>
<li>5.- Estado de salud actual y futuro, orientación sexual o religiosa, origen racial o étnico, información genética.</li>
</ul>

<h2>Estos serán utilizados única y exclusivamente para los siguientes fines:
</h2>

<ul>
<li>1. Poder ofrecer y dar a conocer los servicios en que consiste la actividad profesional que desarrollamos. </li>
<li>2. Cualquier finalidad análoga o compatible con la anterior.</li>
</ul>

<p>Para prevenir el acceso no autorizado a sus datos personales y con el fin de asegurar que la información sea utilizada para los fines establecidos en este aviso de privacidad, hemos establecido diversos procedimientos con la finalidad de evitar el uso o divulgación no autorizados de sus datos, permitiéndonos tratarlos debidamente.Así mismo, le informamos que sus datos personales pueden ser Transmitidos para ser tratados por personas distintas a esta empresa.
</p>

<p>Todos sus datos personales son tratados de acuerdo a la legislación aplicable y vigente en el país, por ello le informamos que usted tiene en todo momento los derechos (ARCO) de acceder, rectificar, cancelar u oponerse al tratamiento que le damos a sus datos personales; derecho que podrá hacer valer a través del Área de Privacidad encargada de la seguridad de datos personales en el Teléfono, o por medio de su correo electrónico: 
</p>

<p>A través de estos canales usted podrá actualizar sus datos y especificar el medio por el cual desea recibir información, ya que en caso de no contar con esta especificación de su parte, INSTITUTO SONORENSE DE INFERTILIDAD  establecerá libremente el canal que considere pertinente para enviarle información.
</p>
       
<p>Este aviso de privacidad podrá ser modificado por INSTITUTO SONORENSE DE INFERTILIDAD, dichas  notificaciones serán  oportunamente informadas a través de correo electrónico, teléfono, o cualquier otro medio de comunicación que INSTITUTO SONORENSE DE INFERTILIDAD  determine para tal efecto.
</p>        
                
