                <h1>Fecundación in Vitro</h1>
                 
                <p>Con objeto de que tenga lugar el embarazo, el ovario debe producir un óvulo maduro y unirse con el espermatozoide. Esta unión, llamada fecundación, fisiológicamente tiene lugar dentro de las Trompas de Falopio. Sin embargo, durante el proceso de FIV, esta unión ocurre en el laboratorio después de haber obtenido tantos óvulos como el ovario haya sido capaz de desarrollar.</p>
                
                <p>Una vez realizada la fecundación, un máximo de tres embriones de los obtenidos, según sea la elección de la pareja, se depositarán en el útero para que continúen su desarrollo.</p>
                
                <h2>Los embriones sobrantes son:</h2>
                
                <ul>
                    <li>Criopreservados para su posterior transferencia en otro ciclo.</li>
                    <li>Donados a otras parejas o</li>
                    <li>Cedidos para investigación con el consentimiento de sus progenitores.</li>
                </ul>

    <p>El éxito de la técnica depende en gran medida de la edad de la mujer, de la respuesta ovárica y de la calidad de los embriones.</p>
    
    <p>En general la tasa de embarazo por ciclo se encuentra entre el 55 y 65%. Aunque en pacientes menores de 35 años supera el 70%.</p>



                
